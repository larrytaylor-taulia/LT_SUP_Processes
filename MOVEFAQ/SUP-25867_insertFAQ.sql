	<li>Wenn Sie zum&nbsp;<strong>ersten Mal</strong>&nbsp;eine vorzeitige Zahlung beantragen m&ouml;chten und Sie mehrere Bankkonten bei Hugo Boss registriert haben, wird dies im Portal unter&nbsp;<strong>&bdquo;Meine Details &gt; Bankkonten&ldquo;</strong>&nbsp;angezeigt. Wenden Sie sich hierbei bitte zuerst an den&nbsp;<a href="https://support.taulia.com/pkb_contact?b=&amp;l=de" target="_blank">Taulia-Support&nbsp;</a>und teilen Sie ihm mit, dass Sie die Angebote zur vorzeitigen Zahlung nicht sehen k&ouml;nnen, weil Sie mehrere Bankkonten haben. W&auml;hlen Sie dann ein Bankkonto aus, welches Sie zuk&uuml;nftig f&uuml;r die vorzeitige Zahlung verwenden m&ouml;chten. Sobald dieses Bankkonto f&uuml;r die vorzeitige Zahlung festgelegt ist, werden Sie auch Angebote f&uuml;r die vorzeitige Zahlung f&uuml;r Ihre genehmigten Rechnungen einsehen k&ouml;nnen.</li>
	<li>Das aktuelle Datum liegt zu nahe am F&auml;lligkeitsdatum der Rechnung.</li>
	<li>Sie haben Gutschriften oder Vorauszahlungstilgungen auf Ihrem Konto, die den Rechnungsbetrag &uuml;bersteigen.</li>
</ul>

